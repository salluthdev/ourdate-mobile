$(document).ready(function()
{
	$('.qrcode-guestbook').click(function()
	{
		$('.popup_box').css(
		{
			"opacity":"1", "margin-top":"0", "pointer-events":"auto"
		});
		$('.dark').css(
		{
			"display":"block"
		});
	});
	$('.close').click(function()
	{
		$('.popup_box').css(
		{
			"opacity":"0", "margin-top":"-100vh", "pointer-events":"none"
		});
		$('.dark').css(
		{
			"display":"none"
		});
	});
	$('.buka-undangan').click(function()
	{
		$('.open-inv').css(
		{
			"margin-top":"-100vh"
		});
	});
});